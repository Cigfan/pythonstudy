orbits = [(1, 3), (2.5, 10), (7, 2), (6, 6), (40, 40)]

def find_farthest_orbit(orb):
   orb = list(filter(lambda x: x[0] != x[1], orb))
   print(orb)
   for i in orb:
      if i[0] * i[1] == max(list(map(lambda x: x[0] * x[1], orb))):
        return i

print(find_farthest_orbit(orbits))

